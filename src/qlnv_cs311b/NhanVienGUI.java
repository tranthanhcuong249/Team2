    private void btthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btthemActionPerformed
        // TODO add your handling code here:
        String ma = this.tfmanhanvien.getText();
        String ht = this.tfhoten.getText();
        double hsl = Double.parseDouble(this.tfhesoluong.getText());
        Date nvl = new Date();
        DateFormat df = new SimpleDateFormat ("yyyy/m/d");
         try {
             nvl = df.parse(this.tfngayvaolam.getText());
         } catch (ParseException ex) {}
         boolean phai = true;
         if (this.rbnam.isSelected())
             phai = true;
         NhanVien v;
         if(this.rbchuyenvien.isSelected()){
             String cv = this.tfcongviec.getText();
             v = new ChuyenVien(cv,ma,ht,hsl,nvl,phai);
             ds.them(v);
             dm.addRow(new Object[]{v.getMaNV(),v.getHoTen(),v.getHeSoLuong(),
               v.getNgayVaoLam(),v.isPhai(), ((ChuyenVien)v).getCongViec()});
         }
         else{
             String cvu = this.tfchucvu.getText();
             double pccv = Double.parseDouble(this.tfphucapchucvu.getText());
             v = new CanBoQuanLy (cvu,pccv,ma,ht,hsl,nvl,phai);            
             ds.them(v);
             dm.addRow(new Object[]{v.getMaNV(),v.getHoTen(),v.getHeSoLuong(),
             v.getNgayVaoLam(),v.isPhai(),
                "",((CanBoQuanLy)v).getChucVu(),((CanBoQuanLy)v).getPhuCapChucVu()});
         }
        
        
    }//GEN-LAST:event_btthemActionPerformed

